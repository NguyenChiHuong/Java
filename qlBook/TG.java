package qlBook;

public class TG {
	
	private String tgia;
	
	private NS ns;

	public TG(String tgia, NS ns) {
		this.tgia = tgia;
		this.ns = ns;
	}

	public String getTgia() {
		return tgia;
	}

	public void setTgia(String tgia) {
		this.tgia = tgia;
	}

	public NS getNs() {
		return ns;
	}

	public void setNs(NS ns) {
		this.ns = ns;
	}

}
